# Logical Operators
# there are some logical operators like and, or, not
print(x > y and x < z) # True and True = True
print(x > y or x < z) # True or True = True
print(not x > y) # not True = False
print(1 == 2 < 3) # False and True = False

# bitwise operators
# there are some bitwise operators like &, |, ^, ~, <<, >>
# & - bitwise AND
# | - bitwise OR
# ^ - bitwise XOR
# ~ - bitwise NOT
# << - bitwise left shift
# >> - bitwise right shift
print(5 & 3) # 1
print(5 | 3) # 7
print(7 ^ 5) # 2
